void display_to_screen() {
    
    SSD1306_t dev; // stwhat ruct for the ssd1306 display

    // initialize i2c comm & display 
    i2c_master_init(&dev, CONFIG_SDA_GPIO, CONFIG_SCL_GPIO, CONFIG_RESET_GPIO);
    ssd1306_init(&dev, DISPLAY_WIDTH, DISPLAY_HEIGHT);
    
    
    ssd1306_clear_screen(&dev, false);
    ssd1306_bitmaps(&dev, 0,0, electrium_logo, 128,64, false);

    // explicitly "push" that buffer to the display
    ssd1306_show_buffer(&dev);

    // Display for 4 seconds with single delay
    ESP_LOGI("DISPLAY", "Displaying Electrium logo");
    vTaskDelay(pdMS_TO_TICKS(4000));

    ssd1306_clear_screen(&dev, false);
    ssd1306_show_buffer(&dev);
    ESP_LOGI("DISPLAY", "Display cleared");

}



